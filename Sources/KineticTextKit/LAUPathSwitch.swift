@MainActor public class LAUPathSwitch: UIControl {
    
    public var toggleState: LAUPathSwitchToggleState = .none
    
    public func setToggle(state: LAUPathSwitchToggleState, animated: Bool) {
        toggleState = state
        let newPath = state.path
        if let oldPath = shapeLayer.path, animated {
            let animation = createAnimation(from: oldPath, to: newPath)
            shapeLayer.add(animation, forKey: "path")
        } else {
            shapeLayer.path = newPath
        }
    }
    
    override public init(frame: CGRect) {
        super.init(frame: frame)
        layer.addSublayer(shapeLayer)
        shapeLayer.frame = layer.bounds
        updatePath()
    }
    
    required public init?(coder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }
    
    private var shapeLayer = CAShapeLayer()
    
    private func updatePath() {
        shapeLayer.path = toggleState.path
    }
    
    public override var tintColor: UIColor! {
        didSet {
            updateTintColor()
        }
    }
    
    private func updateTintColor() {
        shapeLayer.fillColor = tintColor.cgColor
    }
    
    override public func endTracking(_ touch: UITouch?, with event: UIEvent?) {
        setToggle(state: toggleState.toggle(), animated: true)
    }
    
    // MARK: - Helpers
    
    private func createAnimation(from: CGPath, to: CGPath) -> CABasicAnimation {
        let animation = CABasicAnimation(keyPath: "path")
        animation.fromValue = from
        animation.toValue = to
        animation.duration = 2.0
        animation.timingFunction = CAMediaTimingFunction(name: .easeInEaseOut)
        animation.fillMode = .both // why do we need this
        animation.isRemovedOnCompletion = false
        return animation
    }
}
